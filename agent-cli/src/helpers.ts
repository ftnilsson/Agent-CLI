import * as fs from "node:fs";
import * as path from "node:path";
import type { Registry } from "./types.js";

// ─── Types ───────────────────────────────────────────────────────────────────

export interface ResolvedEntry {
  key: string;
  srcPath: string;
  destFolder: string;
}

export type InstallTarget = "copilot" | "claude" | "cursor" | "mixed";

// ─── Functions ───────────────────────────────────────────────────────────────

/**
 * Resolve the output path for agent instructions based on --format flag.
 * @deprecated Use resolveAgentOutputPaths instead
 */
export function resolveAgentOutputPath(
  format: string | undefined,
  manifestDefault?: string,
): string {
  switch (format) {
    case "copilot":
      return ".github/copilot-instructions.md";
    case "cursor":
      return ".cursorrules";
    case "claude":
      return "CLAUDE.md";
    default:
      return manifestDefault ?? "agent.md";
  }
}

/**
 * Resolve output paths for agent instructions based on target.
 * Returns array of paths for the target (1 for single targets, 3 for "mixed").
 */
export function resolveAgentOutputPaths(target: InstallTarget): string[] {
  switch (target) {
    case "copilot":
      return [".github/copilot-instructions.md"];
    case "claude":
      return ["CLAUDE.md"];
    case "cursor":
      return [".cursorrules"];
    case "mixed":
      return [".github/copilot-instructions.md", "CLAUDE.md", ".cursorrules"];
    default:
      const _exhaustive: never = target;
      return _exhaustive;
  }
}

/**
 * Validate and normalize install target from CLI args.
 * Always returns a valid target (defaults to "copilot" or provided default).
 * Supports both --target (preferred) and --format (deprecated) for backward compatibility.
 */
export function parseInstallTarget(
  args: string[],
  defaultTarget?: InstallTarget,
): InstallTarget {
  // Check for --target (required in v2.0)
  const targetIdx = args.indexOf("--target");
  if (targetIdx !== -1 && targetIdx + 1 < args.length) {
    const target = args[targetIdx + 1];
    if (
      target === "copilot" ||
      target === "claude" ||
      target === "cursor" ||
      target === "mixed"
    ) {
      return target;
    }
  }

  // Check for removed flags and provide helpful error messages
  if (args.includes("--format")) {
    throw new Error(
      `--format is no longer supported (removed in v2.0).\nUse --target copilot|claude|cursor|mixed instead.\nExample: agent install --target copilot`,
    );
  }
  if (args.includes("--all")) {
    throw new Error(
      `--all is no longer supported (removed in v2.0).\nUse --target mixed instead.\nExample: agent install --target mixed`,
    );
  }

  return defaultTarget ?? "copilot";
}

/**
 * Compose multiple agent instruction sections into a single file.
 * Adds a generation header and separator between sections.
 */
export function composeAgentFile(sections: string[]): string {
  const header = [
    "<!-- Auto-generated by agent-cli. Do not edit manually. -->",
    "<!-- Run `agent install` to regenerate. -->",
    "",
  ];

  if (sections.length === 1) {
    return [...header, sections[0], ""].join("\n");
  }

  const body = sections.join("\n\n---\n\n");
  return [...header, body, ""].join("\n");
}

/**
 * Resolve include keys into source paths and destination folder names,
 * separating skills from agent instructions based on category type.
 */
export function resolveIncludes(
  includes: string[],
  registry: Registry,
): { skills: ResolvedEntry[]; agents: ResolvedEntry[] } {
  const skills: ResolvedEntry[] = [];
  const agents: ResolvedEntry[] = [];

  for (const entry of includes) {
    const [catKey, skillKey] = entry.split("/");
    const cat = registry.categories[catKey];
    if (!cat) {
      console.warn(`  ⚠  Unknown category "${catKey}" — skipping "${entry}"`);
      continue;
    }

    const folder = cat.skills[skillKey];
    if (!folder) {
      console.warn(`  ⚠  Unknown entry "${skillKey}" in "${catKey}" — skipping`);
      continue;
    }

    const resolved: ResolvedEntry = {
      key: entry,
      srcPath: path.join(cat.path, folder),
      destFolder: folder,
    };

    if (cat.type === "agent") {
      agents.push(resolved);
    } else {
      skills.push(resolved);
    }
  }

  return { skills, agents };
}

/**
 * Find the agent.md file inside a directory.
 */
export function findAgentFile(dir: string): string | null {
  if (!fs.existsSync(dir)) return null;

  // Look for agent.md (primary) or any .md file
  const agentMd = path.join(dir, "agent.md");
  if (fs.existsSync(agentMd)) return agentMd;

  // Fallback: first .md file found
  const entries = fs.readdirSync(dir);
  const md = entries.find((e) => e.endsWith(".md"));
  return md ? path.join(dir, md) : null;
}

